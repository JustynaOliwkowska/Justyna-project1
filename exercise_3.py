#petla for

data = [ 2, 4, 6]

for element in data:
    print(f'Element : {element}')

heroes = ["Luke Skywalker", "Princess Leia", "Han Solo", "Chewbacca", "Obi-Wan Kenobi"]

for charac in heroes:
    print(charac)

for J in range (6):
    print(J)

for K in range (5, 10):
    print(K)

for L in range (3, 10, 2):
    print(L)

result = []
for s in range(4):
    result.append(s)
print(result)

resu = list(range(4))
print(resu)

